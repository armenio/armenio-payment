namespace Armenio\Payment;

/**
 * Class AbstractPayment
 *
 * @package Armenio\Payment
 */
class AbstractPayment
{
    /**
     * @param int $cardNumber
     *
     * @return string
     */
    protected function getCardBrand($cardNumber)
    {
        $brands = [
            '/^(401178|401179|431274|438935|451416|457393|457631|457632|504175|627780|636297|636368|(506699|5067[0-6]\\d|50677[0-8])|(50900\\d|5090[1-9]\\d|509[1-9]\\d{2})|65003[1-3]|(65003[5-9]|65004\\d|65005[0-1])|(65040[5-9]|6504[1-3]\\d)|(65048[5-9]|65049\\d|6505[0-2]\\d|65053[0-8])|(65054[1-9]| 6505[5-8]\\d|65059[0-8])|(65070\\d|65071[0-8])|65072[0-7]|(65090[1-9]|65091\\d|650920)|(65165[2-9]|6516[6-7]\\d)|(65500\\d|65501\\d)|(65502[1-9]|6550[3-4]\\d|65505[0-8]))[0-9]{10,12}/' => 'elo',
            '/^3[47][0-9]{13}/' => 'amex',
            '/^3(?:0[0-5]|[68][0-9])[0-9]{11}/' => 'diners',
            '/^4[0-9]{12}(?:[0-9]{3})/' => 'visa',
            '/^5[1-5][0-9]{14}/' => 'master',
            '/^(5(018|0[23]|[68])|6(39|7))/' => 'maestro',
            '/^6(?:011|5[0-9]{2})[0-9]{12}/' => 'discover',
            '/^(606282\\d{10}(\\d{3})?)|(3841\\d{15})/' => 'hipercard',
            '/^(?:2131|1800|35\\d{3})\\d{11}/' => 'jcb',
            '/^(5019)\\d{12}$/' => 'dankort',
            '/^50[0-9]\\d{14,17}$/' => 'aura',
            '/^(4026|417500|4405|4508|4844|4913|4917)\\d+$/' => 'electron',
            '/^(62|88)\\d{16,17,18,19}$/' => 'unionpay',
        ];

        foreach ($brands as $regex => $brand) {
            if (preg_match($regex, $cardNumber)) {
                return $brand;
            }
        }

        return '';
    }
}
